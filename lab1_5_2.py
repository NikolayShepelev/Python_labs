# Задан целочисленный массив
array = [3, 6, 2, 7, 2, 8, 2]

# Находим минимальный элемент в массиве
min_value = min(array)

# Находим индекс первого минимального элемента
min_index = array.index(min_value)

# Получаем элементы, расположенные перед первым минимальным элементом
elements_before_min = array[:min_index]

# Выводим найденные элементы
print("Элементы, расположенные перед первым минимальным:", elements_before_min)
