# Считываем количество строк
n = int(input("Введите количество строк: "))

# Инициализируем пустое множество для хранения уникальных слов
unique_words = set()

# Цикл для считывания каждой строки текста
for _ in range(n):
    # Считываем строку
    line = input()
    # Используем метод split() для разделения строки на слова
    # Автоматически удаляются все пробельные символы (пробелы, переводы строки, табуляции)
    words = line.split()
    # Добавляем слова в множество unique_words, используя метод update(),
    # который позволяет добавить в множество несколько элементов сразу
    unique_words.update(words)

# Количество уникальных слов равно размеру множества unique_words
print("Количество различных слов в тексте:", len(unique_words))
